float str2float(char* s)
{
    float result = 0.0;

    if (s[0] == '1' && s[3] == 0)//输入浮点数为1.0
        result = 1.0;

    else if (s[2] == '0' && s[3] == 0);//输入浮点数为0.0

    else//0.xxx的情况，精度限制原因，只保留前6位
    {
        //先统计浮点数长度
        float pow = 0.1;
        for (int i = 1; i <= 6 && s[i + 1] != 0; i++)
        {
            result += (s[i + 1] - 48) * pow;
            pow *= 0.1;
        }

    }
    return result;
}

